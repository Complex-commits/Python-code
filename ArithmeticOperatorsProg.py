# Our task is to, read two integers and then print out three lines.
# The first line would consist of: The sum of both integers.
# The second line gives the difference of the two numbers.
# The third line gives us the product of the two integers.
# This problem is found from https://www.hackerrank.com/challenges/python-arithmetic-operators/problem
num = int(input("Hand over the number fool! "))
num2 = int(input("That ain't enough hand over some more fool! "))
sum = num + num2
difference = num - num2
product = num * num2
print(sum)
print(difference)
print(product)
